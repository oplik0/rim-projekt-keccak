RC = [
    0x0000000000000001, 0x0000000000008082, 0x800000000000808A, 0x8000000080008000,
    0x000000000000808B, 0x0000000080000001, 0x8000000080008081, 0x8000000000008009,
    0x000000000000008A, 0x0000000000000088, 0x0000000080008009, 0x000000008000000A,
    0x000000008000808B, 0x800000000000008B, 0x8000000000008089, 0x8000000000008003,
    0x8000000000008002, 0x8000000000000080, 0x000000000000800A, 0x800000008000000A,
    0x8000000080008081, 0x8000000000008080, 0x0000000080000001, 0x8000000080008008
]

# Bit positions: 0, 1, 3, 7, 15, 31, 63
positions = [0, 1, 3, 7, 15, 31, 63]

for i, rc in enumerate(RC):
    bits = []
    for pos in positions:
        bits.append((rc >> pos) & 1)
    # RC7 format: bit6=63, bit5=31, bit4=15, bit3=7, bit2=3, bit1=1, bit0=0
    rc7 = (bits[6] << 6) | (bits[5] << 5) | (bits[4] << 4) | (bits[3] << 3) | (bits[2] << 2) | (bits[1] << 1) | bits[0]
    rc7_str = format(rc7, '07b')
    print(f'RC[{i:2d}] = {rc:016x} -> \"{rc7_str}\"')